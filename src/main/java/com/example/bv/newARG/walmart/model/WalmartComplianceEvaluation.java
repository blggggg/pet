package com.example.bv.newARG.walmart.model;

public class WalmartComplianceEvaluation {
    private String overallRating;
    private String performanceRating;
    private String complianceRating;
    private String packagingRating;
    private String sustainabilityRating;

    public String getOverallRating() { return overallRating; }
    public void setOverallRating(String overallRating) { this.overallRating = overallRating; }

    public String getPerformanceRating() { return performanceRating; }
    public void setPerformanceRating(String performanceRating) { this.performanceRating = performanceRating; }

    public String getComplianceRating() { return complianceRating; }
    public void setComplianceRating(String complianceRating) { this.complianceRating = complianceRating; }

    public String getPackagingRating() { return packagingRating; }
    public void setPackagingRating(String packagingRating) { this.packagingRating = packagingRating; }

    public String getSustainabilityRating() { return sustainabilityRating; }
    public void setSustainabilityRating(String sustainabilityRating) { this.sustainabilityRating = sustainabilityRating; }
}
